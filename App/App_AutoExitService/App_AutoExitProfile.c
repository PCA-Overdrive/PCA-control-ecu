const AppAutoExitMotionStep *AppAutoExitProfile_Get(AppAutoExitDirection direction,
                                                    uint32 *profileCount)
{
    const AppAutoExitMotionStep *profile;
    uint32 count;

    profile = 0;
    count = 0u;

    switch(direction)
    {
        case APP_AUTO_EXIT_DIR_STRAIGHT:
            profile = g_profileStraight;
            count = APP_AUTO_EXIT_ARRAY_COUNT(g_profileStraight);
            break;

        case APP_AUTO_EXIT_DIR_LEFT:
            profile = g_profileLeft;
            count = APP_AUTO_EXIT_ARRAY_COUNT(g_profileLeft);
            break;

        case APP_AUTO_EXIT_DIR_RIGHT:
            profile = g_profileRight;
            count = APP_AUTO_EXIT_ARRAY_COUNT(g_profileRight);
            break;

        default:
            break;
    }

    if(profileCount != 0)
    {
        *profileCount = count;
    }

    return profile;
}
